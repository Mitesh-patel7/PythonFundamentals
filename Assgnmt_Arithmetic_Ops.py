# User Input

F_I = float(input("Enter the first number: "))
S_I = float(input("Enter the second number: "))

# Arithmetic Operations
add = F_I + S_I
sub = F_I - S_I
mul = F_I * S_I
div = F_I / S_I

# result
print("Addition:", round(add))
print("Subtraction:",round(sub))
print("Multiplication:",round(mul))
print("Division:",div)